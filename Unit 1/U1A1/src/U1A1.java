/* This project prints
   out a face.
*/

public class U1A1
{
	public static void main(String[] args)
	{
		// Prints hair
		System.out.println("  /////  ");
		
		// Prints forehead
		System.out.println(" |     | ");
		
		// Prints eyebrows (uses \")
		System.out.println(" | \" \" | ");
		
		// Prints eyes
		System.out.println(" | o o | ");
		
		// Prints ears and nose
		System.out.println("(|  ^  |)");
		
		// Prints mouth and chin (uses \\ and \n)
		System.out.print(" | \\_/ |\n  -----  \n");
	}
}